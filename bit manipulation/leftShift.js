//For left shift we are using "<<" this operators << thats indicating the left shifting of bits
let l = 2;
console.log(l << 1); // here we are doing left shift of 2 by one so it will be change from "0010" = 2 to "0100" = 4 

//example:-
console.log(500 << 1); //ans = 1000 // you can also to right shifting by multiplying by 2
console.log(2 << 2) // = ans = 8 // this is equal to 2 * 2^2 (number * 2^n)